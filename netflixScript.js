"use strict";
let counter1 = 0;
document.getElementById("dq1").addEventListener('click', function () {
    if (counter1 % 2 == 0) {
        document.getElementById("x1").style.transform = 'rotate(45deg)';
        document.getElementById("da1").style.display = 'block';
    } else {
        document.getElementById("x1").style.transform = 'rotate(90deg)';
        document.getElementById("da1").style.display = 'none';
    }
    counter1++;
});
